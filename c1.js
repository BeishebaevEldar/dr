{
  let heart = document.getElementById("heart");
  heart.onclick = () => {
    if (heart.classList == "fa-regular fa-heart") {
      heart.classList = "fa-solid fa-heart";
    } else if (heart.classList == "fa-solid fa-heart") {
      heart.classList = "fa-regular fa-heart";
    }
  };
}

